// ---------------------------------------------------------------------
// 题目描述:
// 坐标离散化
// w*h 的格子上画了 n 条垂直或水平的宽度为 1 的直线.
// 求出这些直线将格子划分成了多少个区域.
// 1 <= w, h <= 1000000
// 1 <= n <= 500
// ---------------------------------------------------------------------
// 思路:
// 由于 w 和 h 的范围较大, 无法直接用 BFS 或者 DFS.
// 而 n 较小, 因此可以先将坐标值离散化处理, 化为 500 * 500 以内的矩阵就可以 BFS 了.
// ---------------------------------------------------------------------